package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		//int data type inde bir tane variable oluşturun 
		// bu variable ı her seferinde 1 artırmak için 3 yöntemi de kullanın.
		
		int sayı1=12;
		sayı1=sayı1+1;
		System.out.println(sayı1);//13
		
		sayı1+=1;
		System.out.println(sayı1);//14
		
		sayı1++;
		System.out.println(sayı1);//15
		
				
		
		

	}

}
