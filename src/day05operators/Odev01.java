package day05operators;

public class Odev01 {

	public static void main(String[] args) {
		int num =20;
		num = num + 1;
		System.out.println(num);	
		
		num+= 1;
		System.out.println(num);
		
		num++;
		System.out.println(num);
		
		num = num - 1;
		System.out.println(num);
		
		num-= 1;
		System.out.println(num);
		
		num--;
		System.out.println(num);
	}

}
