package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		//int data type inde iki variable oluşturun 
		// bu variable ları en kısa yolu kullanarak 1 artırın
		// enkısa yolu sadece 1 artırmak için kullanılır.
		int sayı1 = 12;
		sayı1++;
		//variable isminden sonra ki kere + sembolü yazarsanız 
		//o variable nin değerini 1 artırmış oluruz.
		System.out.println(sayı1);//13
		
		
		int sayı2 = 20;
		sayı2++;
		System.out.println(sayı2);

	}

}
