package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		//int data tipinde iki variable oluşturun  birinciyi  3 , ikinciyi 7 arttırın.
		
		int sayı1 = 12;
		System.out.println(sayı1); //12
		sayı1 = sayı1+3;// increment
		System.out.println(sayı1); //15
		
		
		int sayı2= 20;
		System.out.println(sayı2); //20    1. yol ve uzun
		sayı2 = sayı2+7;
		System.out.println(sayı2);//27    1. yol ve uzun



	}

}
